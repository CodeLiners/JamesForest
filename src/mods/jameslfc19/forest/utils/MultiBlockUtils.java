package mods.jameslfc19.forest.utils;

public class MultiBlockUtils {
	
	protected static String[] slab = new String[]{"brickGranite", "brickLimestone"};
	protected static String[] granite = new String[]{"brickGranite", "carvedGranite"};
	protected static String[] limestone = new String[]{"brickLimestone"};
	
	public static String[] getSlabArray() {
		return slab;
	}
	
	public static String[] getGraniteBrickArray() {
		return granite;
	}
	
	public static String[] getLimestoneBrickArray() {
		return limestone;
	}
}
